@app.route('/register', methods=['POST', 'GET'])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            #User already exists
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for('login'))

        # ignore unexpected argument, that is a bug with pycharm or other IDEs and SQLAlchemy, code works fine
        user = User(email=form.email.data,
                    name=form.username.data,
                    password=generate_password_hash(form.password.data, method='pbkdf2:sha256', salt_length=8)
                    )

        db.session.add(user)
        db.session.commit()

        login_user(user)

        return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=form, current_user=current_user)


@app.route('/login', methods=['POST', 'GET'])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user = User.query.filter_by(email=form.email.data).first()  # find the user by email, since it is unique

        #Email doesn't exist
        if not user:
            flash("Invalid credentials.")
            return redirect(url_for('login'))

        if check_password_hash(user.password, form.password.data):  # compares the hashes of the stored and entered passwords for security
            login_user(user)
            return redirect(url_for('get_all_posts'))
        else:
            flash("Invalid credentials.")
            return redirect(url_for('login'))

    return render_template("login.html",form=form, current_user=current_user)


